#include "libft.h"

static char	*ft_strcpy(char *dest, const char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(const char *s)
{
	char	*dest;
	int		size;

	size = ft_strlen(s);
	dest = malloc(size * sizeof(char) + 1);
	if (dest == NULL)
		return (NULL);
	ft_strcpy(dest, s);
	return (dest);
}
